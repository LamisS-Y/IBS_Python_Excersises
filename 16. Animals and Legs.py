#Exercise 16 Animals and Legs
# Write a program that asks for two integers
# The first represents the number of chickens the farmer has
# The second represents the number of pigs owned by the farmer
# It should print how many legs all the animals have

chickens = int(input ("enter number of chickens"))
chicken_legs= chickens*2
pigs = int(input("enter number of pigs"))
pig_legs= pigs*4
print("number of chicken legs: ",chicken_legs)
print("number of pig legs: ", pig_legs)